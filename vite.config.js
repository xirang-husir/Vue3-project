export default () =>
  defineConfig({
    base: './',
    plugins: [
      vue(),
      // 配置element plus 自动按需导入（默认components 下的文件也会被自动注册）
      AutoImport({
        resolvers: [ElementPlusResolver()]
      }),
      Components({
        resolvers: [ElementPlusResolver()]
      })
    ],
    resolve: {
      alias: {
        '@': fileURLToPath(new URL('./src', import.meta.url))
      }
    }
  })
